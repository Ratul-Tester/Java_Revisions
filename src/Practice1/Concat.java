package Practice1;

public class Concat {
    public static void main(String[] args) {
        int a = 10, b = 20;
        String name = "Ratul";
        System.out.println(a+name);
        System.out.println(a+b+name);
        System.out.println(a+name+b);
        System.out.println(name+a+b);
        System.out.println(name+a+-b);
        System.out.println(a*b+name);
        System.out.println(a/b+name);
        System.out.println(a-b+name);
        System.out.println(a%b+name);

    }
}
