public class Operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = b+a;
        int d = b-a;
        int e = b*a;
        int f = b/a;
        int g = b%a;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
    }
}
